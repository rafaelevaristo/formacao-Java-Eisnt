    private void mostraDetalhesPrato(Prato prato){
    
        this.lblNome.setText(prato.getNome());
        this.lblCalorias.setText(prato.getCalorias()+"");
        this.lblClassificacoes.setText(prato.calcularClassificacaoMedia()+"");
        this.lblTipoCozinha.setText(prato.getTipoCozinha());
        this.lblTipoPrato.setText(prato.getTipoPrato());
    
    }
    
    private Prato GetPratoSelecionado(){
    
        int selectedIndex = lstPratos.getSelectedIndex();        
        String selectedPratoKey = lstPratos.getItem(selectedIndex);        
        return Exerciciotres.ListaDePratos.GetPratos().get(selectedPratoKey);   
    }
    

