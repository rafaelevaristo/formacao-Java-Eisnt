    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        
        Prato pratoSelecionado = this.GetPratoSelecionado();
        
        if (Exerciciotres.ListaDePratos.removerPrato(pratoSelecionado)){
            this.dispose();
            new JanelaMenuInicial().setVisible(true);  
        }
        
        
        
    }//GEN-LAST:event_btnApagarActionPerformed

    private void btnClassificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClassificarActionPerformed
         //Mostrar um janela para sair
        Object[] op = {"1 estrela","2 estrelas","3 estrelas", "4 estrelas", "5 estrelas"};
        int classificacao;
       classificacao =  JOptionPane.showOptionDialog(null,"Classifique o prato"
               ,"Clasificar",0, JOptionPane.QUESTION_MESSAGE,
                null,op,op[1]);
       if(classificacao >= 0){
           Prato pratoSelecionado = this.GetPratoSelecionado();
           pratoSelecionado.atribuirClassificacao(classificacao+1);
            this.dispose();
            new JanelaMenuInicial().setVisible(true);  
       }
       
       
       
              
    }//GEN-LAST:event_btnClassificarActionPerformed

